
public class CachTinhDiem {
	double phanTramDiemBGK;
	double phanTramPB;
	public CachTinhDiem(double phanTramDiemBGK, double phanTramPB) {
		super();
		this.phanTramDiemBGK = phanTramDiemBGK;
		this.phanTramPB = phanTramPB;
	}

	public double getPhanTramDiemBGK() {
		return phanTramDiemBGK;
	}

	public double getPhanTramPB() {
		return phanTramPB;
	}

}
