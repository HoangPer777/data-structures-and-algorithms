package Set;

public class Student {
	String id;
	String firstName;
	String lastName;
	int birthYear;
	double score;

	public Student(String id, String firstName, String lastName, int birthYear, double score) {
		super();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.birthYear = birthYear;
		this.score = score;
	}
}
